package bbs_join;

public class Bbs_join {
	private int joinID;
	private String userID;
	private String userPhone;
	private String joinPassword;
	private String joinMember;
	private String joinContent;
	private int moneyCheck;
	
	public int getJoinID() {
		return joinID;
	}
	public void setJoinID(int joinID) {
		this.joinID = joinID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getJoinPassword() {
		return joinPassword;
	}
	public void setJoinPassword(String joinPassword) {
		this.joinPassword = joinPassword;
	}
	public String getJoinMember() {
		return joinMember;
	}
	public void setJoinMember(String joinMember) {
		this.joinMember = joinMember;
	}
	public String getJoinContent() {
		return joinContent;
	}
	public void setJoinContent(String joinContent) {
		this.joinContent = joinContent;
	}
	public int getMoneyCheck() {
		return moneyCheck;
	}
	public void setMoneyCheck(int moneyCheck) {
		this.moneyCheck = moneyCheck;
	}
	
}
